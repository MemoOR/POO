package com.gor.PracticaTable;

public class Coordenada {
	private double xx, yy;
	
	public Coordenada(double x, double y) {
		this.xx = x;
		this.yy = y;
	}

	public double getXx() {
		return xx;
	}

	public void setXx(double xx) {
		this.xx = xx;
	}

	public double getYy() {
		return yy;
	}

	public void setYy(double yy) {
		this.yy = yy;
	}
}
